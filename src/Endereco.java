public class Endereco {
    public String pais;
    public String estado;
    public String rua;
    public String numero;
    public String CEP;

    public Endereco(String pais, String estado, String rua, String numero, String CEP){
        this.pais = pais;
        this.estado = estado;
        this.rua = rua;
        this.numero = numero;
        this.CEP = CEP;
    }

}
